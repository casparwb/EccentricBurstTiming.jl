const m₁₂ = 1

function γ(e, V)
    return 1 + e*cos(V)
end


function get_R(model)
   model.p₃/(1 + model.e₃*cos(model.V₃ᵢ₋₁))
end

function get_p_next(model)
   (; η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₃) = model

   Δp_2b = (128π/5*η*sqrt((m₁₂/pᵢ₋₁)^5))*(1 + 7/8*eᵢ₋₁^2)

   R = get_R(model)

   Δp_3b = if !iszero(m₃)
      (-15π*(2sin(2ιᵢ₋₁ -2ωᵢ₋₁) + 
             4sin(2ωᵢ₋₁) - 2sin(2(ιᵢ₋₁ + ωᵢ₋₁)) - 
             4sin(2V₃ᵢ₋₁ - ιᵢ₋₁ - 2(ωᵢ₋₁ + Ωᵢ₋₁ - ω₃)) - 
             4sin(2V₃ᵢ₋₁ + ιᵢ₋₁ - 2(ωᵢ₋₁ + Ωᵢ₋₁ - ω₃)) - 
             6sin(2(V₃ᵢ₋₁ - ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) - 
             sin(2(V₃ᵢ₋₁ - ιᵢ₋₁ - ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) - 
             sin(2(V₃ᵢ₋₁ + ιᵢ₋₁ - ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) + 
             6sin(2(V₃ᵢ₋₁ + ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) + 
             sin(2(V₃ᵢ₋₁ - ιᵢ₋₁ + ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) + 
             sin(2(V₃ᵢ₋₁ + ιᵢ₋₁ + ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) - 
             4sin(2V₃ᵢ₋₁ - ιᵢ₋₁ + 2(ωᵢ₋₁ - Ωᵢ₋₁ + ω₃)) - 
             4sin(2V₃ᵢ₋₁ + ιᵢ₋₁ + 2(ωᵢ₋₁ - Ωᵢ₋₁ + ω₃))
             )*eᵢ₋₁^2*m₃*pᵢ₋₁^3)/(16*m₁₂*R^3*(1 - eᵢ₋₁^2)^(7/2)) # changed from pᵢ₋₁^4 to pᵢ₋₁^3
   else
      0.0
   end

   return pᵢ₋₁*(1 - Δp_2b + Δp_3b)
end

