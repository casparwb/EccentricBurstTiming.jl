function get_R(model)
    return model.p₃ / (1 + model.e₃ * cos(model.V₃ᵢ₋₁))
end

function get_R(p₃, e₃, V₃ᵢ₋₁)
    return p₃ / (1 + e₃ * cos(V₃ᵢ₋₁))
end

function get_p_next(η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₃, R)

    Δp_2b = (128π / 5 * η * sqrt((m₁₂ / pᵢ₋₁)^5)) * (1 + 7 / 8 * eᵢ₋₁^2)


    Δp_3b = if !iszero(m₃)
        # (
        #     -15 * π * (
        #         sin(2 * ωᵢ₋₁) * (
        #             1 + 3 * cos(2 * (V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)) -
        #                 2 * cos(2 * ιᵢ₋₁) * sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)^2
        #         ) -
        #             4 * cos(ιᵢ₋₁) * cos(2 * ωᵢ₋₁) * sin(2 * (V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃))
        #     ) * eᵢ₋₁^2 * m₃ * pᵢ₋₁^3
        # ) / (4 * m₁₂ * R^3 * sqrt(1 - eᵢ₋₁^2)^7)
        (-30 * eᵢ₋₁^2 * pᵢ₋₁^3 * π * (cos(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃) * sin(ωᵢ₋₁) + cos(ιᵢ₋₁) * 
        cos(ωᵢ₋₁) * sin(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃)) * (cos(ωᵢ₋₁) * cos(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃) - 
        cos(ιᵢ₋₁) * sin(ωᵢ₋₁) * sin(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃)) * m₃) / (sqrt(1 - eᵢ₋₁^2)^7 * m₁₂ * R^3)

    else
        0.0
    end

    return pᵢ₋₁ * (1 - Δp_2b + Δp_3b)
end

function get_e_next(η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₃, R)

    Δe_2b = (608 * π / 15 * η * sqrt((m₁₂ / pᵢ₋₁)^5)) * (1 + 121 / 304 * eᵢ₋₁^2)

    Δe_3b = if !iszero(m₃)
        # (
        #     15 * π * (
        #         sin(2 * ωᵢ₋₁) * (
        #             1 + 3 * cos(2 * (V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)) -
        #                 2 * cos(2 * ιᵢ₋₁) * sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)^2
        #         ) -
        #             4 * cos(ιᵢ₋₁) * cos(2 * ωᵢ₋₁) * sin(2 * (V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃))
        #     ) * m₃ * pᵢ₋₁^3
        # ) / (8 * m₁₂ * R^3 * sqrt(1 - eᵢ₋₁^2)^5)
        (15 * pᵢ₋₁^3 * π * (cos(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃) * sin(ωᵢ₋₁) + cos(ιᵢ₋₁) * cos(ωᵢ₋₁) * 
        sin(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃)) * (cos(ωᵢ₋₁) * cos(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃) - 
        cos(ιᵢ₋₁) * sin(ωᵢ₋₁) * sin(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃)) * m₃) / (sqrt(1 - eᵢ₋₁^2)^5 * m₁₂ * R^3)

    else
        0.0
    end


    # println(Δe_3b)

    return eᵢ₋₁ * (1 - Δe_2b + Δe_3b)
end


function get_Ω_next(η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₃, R)

    ΔΩ = if !iszero(m₃)
        # (
        #     -3 * π * (
        #         4 * cos(ιᵢ₋₁) * sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)^2 +
        #             (
        #             2 * cos(ιᵢ₋₁) * (3 - 5 * cos(2 * ωᵢ₋₁)) * sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)^2 +
        #                 5 * sin(2 * ωᵢ₋₁) * sin(2 * (V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃))
        #         ) * eᵢ₋₁^2
        #     ) * m₃ * pᵢ₋₁^3
        # ) / (4 * m₁₂ * R^3 * sqrt(1 - eᵢ₋₁^2)^7)
        (-3 * pᵢ₋₁^3 * π * (-5 * eᵢ₋₁^2 * sin(2 * ωᵢ₋₁) * sin(2 * Ωᵢ₋₁ - 2 * V₃ᵢ₋₁ - 2 * ω₃) + 
        2 * cos(ιᵢ₋₁) * (2 + 3 * eᵢ₋₁^2 - 5 * eᵢ₋₁^2 * cos(2 * ωᵢ₋₁)) * sin(Ωᵢ₋₁ - V₃ᵢ₋₁ - ω₃)^2) * m₃) / (4 * sqrt(1 - eᵢ₋₁^2)^7 * m₁₂ * R^3)

    else
        0.0
    end

    return Ωᵢ₋₁ + ΔΩ
end

function get_ω_next(η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₃, R)


