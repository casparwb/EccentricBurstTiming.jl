          ιᵢ₋₁))*eᵢ₋₁*m₃*
     pᵢ₋₁^3)/(32*m₁₂*R^3*(1 - eᵢ₋₁^2)^(5/2))
   else
      0.0
   end

   return eᵢ₋₁*(1 - Δe_2b + Δe_3b)
end


