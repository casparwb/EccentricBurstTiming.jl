   R = get_R(model)

   ΔΩ = if !iszero(m₃)
      (3*π*sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)*(-2*cos(ιᵢ₋₁)*
        sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃) + (-5*cos(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)*
         sin(2*ωᵢ₋₁) + (-3 + 5*cos(2*ωᵢ₋₁))*
