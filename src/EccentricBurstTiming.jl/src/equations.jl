    Δt_2b = 2π / √m₁₂ * √((pᵢ/(1 - eᵢ^2))^3)

    Δt_3b = if !iszero(m₃)
        # 1 - (
        #     (
        #         8 * (
        #             5 - 12 * cos(2 * ι_next - 2 * ω_next) - 12 * cos(2 * (ι_next + ω_next)) +
        #                 24 * cos(2 * ω_next) * (1 + 3 * cos(2 * (V₃_next - Ω_next + ω₃))) +
        #                 3 * cos(2 * ι_next) * (5 + 8 * cos(2 * ω_next) * cos(2 * (V₃_next - Ω_next + ω₃))) +
        #                 30 * cos(2 * (V₃_next - Ω_next + ω₃)) * sin(ι_next)^2 +
        #                 96 * cos(ι_next) * sin(2 * ω_next) * sin(2 * (V₃_next - Ω_next + ω₃))
        #         ) +
        #             3 * (
        #             10 - 17 * cos(2 * ι_next - 2 * ω_next) - 17 * cos(2 * (ι_next + ω_next)) +
        #                 34 * cos(2 * ω_next) * (1 + 3 * cos(2 * (V₃_next - Ω_next + ω₃))) +
        #                 cos(2 * ι_next) * (30 + 34 * cos(2 * ω_next) * cos(2 * (V₃_next - Ω_next + ω₃))) +
        #                 60 * cos(2 * (V₃_next - Ω_next + ω₃)) * sin(ι_next)^2 +
        #                 136 * cos(ι_next) * sin(2 * ω_next) * sin(2 * (V₃_next - Ω_next + ω₃))
        #         ) * e_next^2
        #     ) * m₃ * p_next^3
        # ) / (128 * m₁₂ * R^3 * (e_next^2 - 1)^3)
        1 - (pᵢ^3 * (80 + 60 * eᵢ^2 + 60 * (4 + 3 * eᵢ^2) * cos(2 * ιᵢ) - 
        6 * (32 + 17 * eᵢ^2) * cos(2 * ιᵢ - 2 * ωᵢ) - 192 * cos(2 * (ιᵢ + ωᵢ)) + 
        96 * cos(2 * Ωᵢ - 2 * V₃ᵢ - 2 * ω₃) * (4 * (3 + cos(2 * ιᵢ)) * cos(2 * ωᵢ) + 
        5 * sin(ιᵢ)^2) - 1536 * cos(ιᵢ) * sin(2 * ωᵢ) * sin(2 * Ωᵢ - 2 * V₃ᵢ - 2 * ω₃) + 
        3 * (4 * (32 + 17 * eᵢ^2) * cos(2 * ωᵢ) + eᵢ^2 * (-34 * cos(2 * (ιᵢ + ωᵢ)) + 
        4 * cos(2 * Ωᵢ - 2 * V₃ᵢ - 2 * ω₃) * (17 * (3 + cos(2 * ιᵢ)) * cos(2 * ωᵢ) + 
