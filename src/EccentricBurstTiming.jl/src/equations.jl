   Δe_3b = if !iszero(m₃)
(15*π*(4*sin(2*ωᵢ₋₁) - 
       6*sin(2*(V₃ᵢ₋₁ - ωᵢ₋₁ -  Ωᵢ₋₁ +  ω₃)) + 
