function get_ι_next(model)
   (; ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ω₃, ωᵢ₋₁, eᵢ₋₁, pᵢ₋₁, m₃, m₁₂) = model

   R = get_R(model)

   Δi = if !iszero(m₃)
      (-3*π*sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)*sin(ιᵢ₋₁)*(4*cos(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃) + 
       (6*cos(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃) + 5*(cos(V₃ᵢ₋₁ - 2*ωᵢ₋₁ - Ωᵢ₋₁ + ω₃) + cos(V₃ᵢ₋₁ + 2*ωᵢ₋₁ - Ωᵢ₋₁ + ω₃) + 
       2*cos(ιᵢ₋₁)*sin(2*ωᵢ₋₁)*sin(V₃ᵢ₋₁ - Ωᵢ₋₁ + ω₃)))*eᵢ₋₁^2)*m₃*pᵢ₋₁^3)/(4*m₁₂*R^3*(1 - eᵢ₋₁^2)^(7/2))
   else
      0.0
   end

   return ιᵢ₋₁ + Δi
end

function get_V₃ᵢ₋₁_next(model)
   (; V₃ᵢ₋₁, sqrt_Mp₃⁻³, e₃, t, tᵢ₋₁) = model

    return V₃ᵢ₋₁ + sqrt_Mp₃⁻³*γ(e₃, V₃ᵢ₋₁)^2*(t - tᵢ₋₁)
end

