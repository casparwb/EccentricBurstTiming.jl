
function γ(e, V)
    return 1 + e*cos(V)
end


function get_R(model)
   model.p₃/(1 + model.e₃*cos(model.V₃ᵢ₋₁))
end

function get_p_next(model)
   (; η, ιᵢ₋₁, V₃ᵢ₋₁, Ωᵢ₋₁, ωᵢ₋₁, ω₃, eᵢ₋₁, pᵢ₋₁, m₁₂, m₃) = model

   Δp_2b = (128π/5*η*sqrt((m₁₂/pᵢ₋₁)^5))*(1 + 7/8*eᵢ₋₁^2)

   R = get_R(model)

   Δp_3b = if !iszero(m₃)
      (-15*π*(4*sin(2*ωᵢ₋₁) - 
       6*sin(2*(V₃ᵢ₋₁ - ωᵢ₋₁ -  Ωᵢ₋₁ +  ω₃)) + 
       6*sin(2*(V₃ᵢ₋₁ + ωᵢ₋₁ -  Ωᵢ₋₁ +  ω₃)) - 
       2*sin(2*ωᵢ₋₁ - 2*ιᵢ₋₁) - 
       sin(2*(V₃ᵢ₋₁ - ωᵢ₋₁ - Ωᵢ₋₁ + ω₃ - ιᵢ₋₁)) + 
       sin(2*(V₃ᵢ₋₁ + ωᵢ₋₁ - Ωᵢ₋₁ + ω₃ - ιᵢ₋₁)) - 
       4*sin(2*V₃ᵢ₋₁ - 2*ωᵢ₋₁ - 2*Ωᵢ₋₁ + 2*ω₃ - ιᵢ₋₁) - 
       4*sin(2*V₃ᵢ₋₁ + 2*ωᵢ₋₁ - 2*Ωᵢ₋₁ + 2*ω₃ - ιᵢ₋₁) - 
       2*sin(2*(ωᵢ₋₁ + ιᵢ₋₁)) - 
       sin(2*(V₃ᵢ₋₁ - ωᵢ₋₁ - Ωᵢ₋₁ + ω₃ + ιᵢ₋₁)) + 
       sin(2*(V₃ᵢ₋₁ + ωᵢ₋₁ - Ωᵢ₋₁ + ω₃ + ιᵢ₋₁)) - 
       4*sin(2*V₃ᵢ₋₁ - 2*ωᵢ₋₁ - 2*Ωᵢ₋₁ + 2*ω₃ + ιᵢ₋₁) - 
       4*sin(2*V₃ᵢ₋₁ + 2*ωᵢ₋₁ - 2*Ωᵢ₋₁ + 2*ω₃ + ιᵢ₋₁))*eᵢ₋₁^2*m₃*
     pᵢ₋₁^4)/(16*m₁₂*R^3*(1 - eᵢ₋₁^2)^(7/2))
   else
      0.0
   end

   return pᵢ₋₁*(1 - Δp_2b + Δp_3b)
end

